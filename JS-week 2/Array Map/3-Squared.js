// Square
// A square is the result of taking a number and multiplying it by itself:

// squared(1); // 1
// squared(2); // 4
// squared(3); // 9
//  Your Goal: Square it
// Take a single number n and return n squared.

/**
 * Given a number n, square it.
 *
 * @param {number} n 
 * @return {number} n squared
 */
 function squared(n) {
    return n*n
}

module.exports = squared;